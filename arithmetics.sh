#!/usr/bin/bash

num=45
num1=5

echo "Sum of $num and $num1 is" $(( num + num1 ))
echo "Subtraction of $num and $num1 is" $(( num - num1 ))
echo "Division of $num and $num1 is" $(( num / num1 ))
echo "Product of $num and $num1 is" $(( num * num1 ))
echo "Modulos of $num and $num1 is" $(( num % num1 ))
